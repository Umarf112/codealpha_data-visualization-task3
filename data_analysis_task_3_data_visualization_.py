def create_portfolio_visualizations():
    """High-impact visualizations for portfolio"""

    # Visualization 1: Correlation Heatmap
    fig1, ax1 = plt.subplots(figsize=(10, 8))
    numeric_cols = ['revenue', 'customer_age', 'rating', 'margin', 'returned']
    corr_matrix = df[numeric_cols].corr()
    mask = np.triu(np.ones_like(corr_matrix, dtype=bool))
    sns.heatmap(corr_matrix, mask=mask, annot=True, fmt='.2f', cmap='coolwarm',
                center=0, square=True, linewidths=1, cbar_kws={'shrink': 0.8},
                ax=ax1)
    ax1.set_title('Feature Correlation Matrix', fontsize=16, fontweight='bold', pad=20)
    plt.tight_layout()
    plt.show()

    # Visualization 2: Violin Plot with Swarm
    fig2, ax2 = plt.subplots(figsize=(12, 6))
    sns.violinplot(x='product', y='revenue', data=df, ax=ax2, palette='Set3', alpha=0.7)
    sns.swarmplot(x='product', y='revenue', data=df, ax=ax2, color='black', alpha=0.3, size=3)
    ax2.set_title('Revenue Distribution by Product (Violin + Swarm)', fontsize=14, fontweight='bold')
    ax2.set_xlabel('Product')
    ax2.set_ylabel('Revenue ($)')
    ax2.tick_params(axis='x', rotation=45)
    plt.tight_layout()
    plt.show()

    # Visualization 3: Time Series with Multiple Metrics
    fig3, ax3 = plt.subplots(figsize=(14, 6))
    daily_metrics = df.groupby(df['date'].dt.date).agg({
        'revenue': 'sum',
        'rating': 'mean'
    }).reset_index()

    ax3.plot(daily_metrics['date'], daily_metrics['revenue']/1000,
             color='#2E86AB', linewidth=2, label='Daily Revenue ($K)')
    ax3.set_xlabel('Date')
    ax3.set_ylabel('Revenue ($K)', color='#2E86AB')
    ax3.tick_params(axis='y', labelcolor='#2E86AB')

    ax3_twin = ax3.twinx()
    ax3_twin.plot(daily_metrics['date'], daily_metrics['rating'],
                  color='#E74C3C', linewidth=2, linestyle='--', label='Avg Rating')
    ax3_twin.set_ylabel('Average Rating', color='#E74C3C')
    ax3_twin.tick_params(axis='y', labelcolor='#E74C3C')

    ax3.set_title('Revenue vs Customer Satisfaction Over Time', fontsize=14, fontweight='bold')
    ax3.legend(loc='upper left')
    ax3_twin.legend(loc='upper right')
    plt.tight_layout()
    plt.show()

    # Visualization 4: Treemap-style Bar Chart (Product × Region)
    fig4, ax4 = plt.subplots(figsize=(12, 6))
    pivot_data = df.pivot_table(values='revenue', index='product', columns='region', aggfunc='sum')
    pivot_data.plot(kind='bar', stacked=True, ax=ax4, colormap='viridis', edgecolor='black')
    ax4.set_title('Revenue Breakdown: Product × Region', fontsize=14, fontweight='bold')
    ax4.set_xlabel('Product')
    ax4.set_ylabel('Revenue ($M)')
    ax4.legend(title='Region', bbox_to_anchor=(1, 1))
    ax4.tick_params(axis='x', rotation=45)
    plt.tight_layout()
    plt.show()

    print("    Advanced visualizations created!")

create_portfolio_visualizations()

