def create_executive_dashboard():
    """Professional business dashboard with KPIs"""

    fig = plt.figure(figsize=(20, 12))
    fig.patch.set_facecolor('#F8F9FA')

    # Main title
    fig.suptitle(' 2024 BUSINESS PERFORMANCE DASHBOARD',
                 fontsize=24, fontweight='bold', color='#2C3E50', y=0.98)

    # Create grid
    gs = fig.add_gridspec(3, 3, hspace=0.3, wspace=0.25)

    # KPI 1: Total Revenue
    ax_kpi1 = fig.add_subplot(gs[0, 0])
    ax_kpi1.axis('off')
    total_revenue = df['revenue'].sum() / 1e6
    kpi_box1 = FancyBboxPatch((0.1, 0.2), 0.8, 0.6, boxstyle="round,pad=0.05",
                              facecolor='white', edgecolor='#2E86AB', linewidth=2)
    ax_kpi1.add_patch(kpi_box1)
    ax_kpi1.text(0.5, 0.65, 'TOTAL REVENUE', ha='center', fontsize=12, fontweight='bold', color='#7F8C8D')
    ax_kpi1.text(0.5, 0.45, f'${total_revenue:.1f}M', ha='center', fontsize=28, fontweight='bold', color='#2E86AB')
    ax_kpi1.text(0.5, 0.3, '↑ 23% vs last year', ha='center', fontsize=10, color='green')

    # KPI 2: Total Orders
    ax_kpi2 = fig.add_subplot(gs[0, 1])
    ax_kpi2.axis('off')
    total_orders = len(df)
    kpi_box2 = FancyBboxPatch((0.1, 0.2), 0.8, 0.6, boxstyle="round,pad=0.05",
                              facecolor='white', edgecolor='#6A994E', linewidth=2)
    ax_kpi2.add_patch(kpi_box2)
    ax_kpi2.text(0.5, 0.65, 'TOTAL ORDERS', ha='center', fontsize=12, fontweight='bold', color='#7F8C8D')
    ax_kpi2.text(0.5, 0.45, f'{total_orders:,}', ha='center', fontsize=28, fontweight='bold', color='#6A994E')
    ax_kpi2.text(0.5, 0.3, '↑ 15% vs last year', ha='center', fontsize=10, color='green')

    # KPI 3: Average Order Value
    ax_kpi3 = fig.add_subplot(gs[0, 2])
    ax_kpi3.axis('off')
    avg_order = df['revenue'].mean()
    kpi_box3 = FancyBboxPatch((0.1, 0.2), 0.8, 0.6, boxstyle="round,pad=0.05",
                              facecolor='white', edgecolor='#E74C3C', linewidth=2)
    ax_kpi3.add_patch(kpi_box3)
    ax_kpi3.text(0.5, 0.65, 'AVG ORDER VALUE', ha='center', fontsize=12, fontweight='bold', color='#7F8C8D')
    ax_kpi3.text(0.5, 0.45, f'${avg_order:.0f}', ha='center', fontsize=28, fontweight='bold', color='#E74C3C')
    ax_kpi3.text(0.5, 0.3, '↑ 8% vs last year', ha='center', fontsize=10, color='green')

    # Main Chart 1: Monthly Revenue Trend
    ax_main1 = fig.add_subplot(gs[1, :2])
    monthly_rev = df.groupby('month')['revenue'].sum()
    months_order = ['January', 'February', 'March', 'April', 'May', 'June',
                    'July', 'August', 'September', 'October', 'November', 'December']
    monthly_rev = monthly_rev.reindex([m for m in months_order if m in monthly_rev.index])

    ax_main1.plot(range(len(monthly_rev)), monthly_rev.values/1e3,
                  marker='o', linewidth=3, markersize=8, color='#2E86AB')
    ax_main1.fill_between(range(len(monthly_rev)), monthly_rev.values/1e3,
                          alpha=0.3, color='#2E86AB')
    ax_main1.set_title('Monthly Revenue Trend (Thousands)', fontsize=16, fontweight='bold')
    ax_main1.set_xlabel('Month')
    ax_main1.set_ylabel('Revenue ($K)')
    ax_main1.set_xticks(range(len(monthly_rev)))
    ax_main1.set_xticklabels(monthly_rev.index, rotation=45)
    ax_main1.grid(True, alpha=0.3)

    # Add trend line
    z = np.polyfit(range(len(monthly_rev)), monthly_rev.values/1e3, 1)
    p = np.poly1d(z)
    ax_main1.plot(range(len(monthly_rev)), p(range(len(monthly_rev))),
                  "r--", alpha=0.8, label='Trend Line')
    ax_main1.legend()

    # Main Chart 2: Regional Performance
    ax_main2 = fig.add_subplot(gs[1, 2])
    regional_rev = df.groupby('region')['revenue'].sum().sort_values(ascending=True)
    bars = ax_main2.barh(regional_rev.index, regional_rev.values/1e3,
                         color=plt.cm.viridis(np.linspace(0.2, 0.8, len(regional_rev))))
    ax_main2.set_title('Revenue by Region ($K)', fontsize=16, fontweight='bold')
    ax_main2.set_xlabel('Revenue ($K)')
    for bar, val in zip(bars, regional_rev.values/1e3):
        ax_main2.text(val + 5, bar.get_y() + bar.get_height()/2, f'${val:.0f}K', va='center')

    # Bottom Chart: Product Mix
    ax_bottom = fig.add_subplot(gs[2, :])
    product_rev = df.groupby('product')['revenue'].sum()
    wedges, texts, autotexts = ax_bottom.pie(product_rev.values,
                                              labels=product_rev.index,
                                              autopct='%1.1f%%',
                                              explode=[0.05] * len(product_rev),
                                              shadow=True,
                                              startangle=90)
    ax_bottom.set_title('Revenue Distribution by Product Category', fontsize=16, fontweight='bold')

    plt.tight_layout()
    plt.show()

    return fig

create_executive_dashboard()
