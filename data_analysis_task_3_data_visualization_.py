# Generate realistic raw data (simulating messy data)
np.random.seed(2024)

# Raw data with inconsistencies
raw_data = {
    'transaction_id': range(1, 1001),
    'date': pd.date_range('2024-01-01', '2024-12-31', freq='8H')[:1000],
    'sales_amount': np.random.gamma(2, 150, 1000) + 50,
    'customer_age': np.random.normal(35, 12, 1000),
    'product': np.random.choice(['Laptop', 'Phone', 'Tablet', 'Headphones', 'Charger'], 1000),
    'region': np.random.choice(['North America', 'Europe', 'Asia', 'South America', 'Africa'], 1000),
    'customer_type': np.random.choice(['New', 'Regular', 'VIP'], 1000, p=[0.5, 0.35, 0.15]),
    'returned': np.random.choice([0, 1], 1000, p=[0.85, 0.15]),
    'rating': np.random.choice([1, 2, 3, 4, 5], 1000, p=[0.05, 0.1, 0.2, 0.35, 0.3])
}

df = pd.DataFrame(raw_data)

# Data transformation
print("   • Cleaning missing values...")
print("   • Creating derived metrics...")
print("   • Aggregating for insights...")

# Clean and transform
df['date'] = pd.to_datetime(df['date'])
df['month'] = df['date'].dt.strftime('%B')
df['quarter'] = 'Q' + df['date'].dt.quarter.astype(str)
df['weekday'] = df['date'].dt.day_name()
df['revenue'] = df['sales_amount'].round(2)
df['profit'] = df['revenue'] * np.random.uniform(0.2, 0.4, len(df))
df['margin'] = (df['profit'] / df['revenue'] * 100).round(1)

# Calculate key metrics
daily_sales = df.groupby(df['date'].dt.date)['revenue'].sum().reset_index()
daily_sales.columns = ['date', 'daily_revenue']

print("    Data transformation complete!\n")

